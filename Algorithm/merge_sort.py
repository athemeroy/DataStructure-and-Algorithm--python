def merge_sort_a(array):
    """
    最开始写的归并排序
    没有分治的过程，所以没有使用递归进行求解。
    直接暴力把整个列表拆分成一个一个的列表，然后直接进行merge操作。
    :return: 排序好的列表
    """

    def merge(left, right):
        """
        归并排序的合并部分
        :param left: 左侧列表
        :param right: 右侧列表
        :return: 按照大小合并之后的列表
        """
        if right is None:
            return left
        merged_result = []
        i = 0
        j = 0
        for k in range(len(left) + len(right)):
            if i == len(left):
                merged_result.append(right[j])
                j += 1
            elif j == len(right):
                merged_result.append(left[i])
                i += 1
            elif left[i] < right[j]:
                merged_result.append(left[i])
                i += 1
            else:
                merged_result.append(right[j])
                j += 1
        return merged_result

    # 用来分割列表， 生成一个包含仅由一个一个元素组成的列表的
    divided = [[i] for i in array]

    while len(divided) > 1:
        if len(divided) % 2:
            divided.append(None)

        # 就用一个新的列表组成两两合并之后的列表
        merged = [merge(divided[i], divided[i + 1]) for i in range(len(divided))[::2]]
        # 用一个新的列表组成两两合并之后的列表
        divided = merged
        # print(divided)
    # 因为上面是最后append了merge之后的结果，所以最后得到的列表一定是Vector[Vector, None]的形式
    # 所以最后取列表的第一个就可以了
    return divided[0]


def merge_sort_b(array):
    """
    第二个版本的归并排序
    在merge函数中，使用了一个新的列表来装载merge后的结果
    如果任意一个分支已经被遍历结束，那么new列表会将剩下的所有内容加入到自身的后面
    并把原来的array从lo到hi的内容覆盖为new的元素。
    :param array: 要排序的列表
    :return: None
    """

    def merge(lo, mid, hi):
        cur_l = lo
        cur_r = mid
        new = []
        while cur_l < mid and cur_r < hi:
            if array[cur_l] < array[cur_r]:
                new.append(array[cur_l])
                cur_l += 1
            else:
                new.append(array[cur_r])
                cur_r += 1
        else:
            new = new + array[cur_l:mid] + array[cur_r:hi]
            array[lo:hi] = new
            # print(f'merged {new}')

    def merge_sort_b_range(lo, hi):
        if hi - lo >= 2:
            mid = (hi + lo) >> 1
            # print(f'merge_sorting lo:{lo},  mid:{mid}')
            merge_sort_b_range(lo, mid)
            # print(f'merge_sorting mid:{mid}, hi:{hi}')
            merge_sort_b_range(mid, hi)
            merge(lo, mid, hi)
        else:
            # print(f'ending as {array[lo:hi]}')
            return

    return merge_sort_b_range(0, len(array))


def merge_sort_c(array):
    """
    第三个版本的归并排序
    看了Mooc之后，发现其实并不需要新列表，我们完全可以直接在原来的列表上进行改动
    同时，如果左边部分遍历结束，而右边部分并没有，后面的我们就完全不需要变动了
    而如果右边遍历结束，则需要用左边的内容覆盖元列表
    这种排序方式需要复制一份左边的列表，使用三个指针：左边、右边、原来

    注：但是这种方式事实上要比b方式慢一丢丢。因为要一个一个改变原列表的元素，而不像
    b方式直接append然后全面覆盖
    In [59]: %timeit(b())
    10 loops, best of 3: 23.8 ms per loop

    In [60]: %timeit(c())
    10 loops, best of 3: 26.8 ms per loop
    :param array: 要排序的列表
    :return: None
    """

    def merge(lo, mid, hi):
        left = array[lo:mid]
        cur_l = lo
        cur_r = mid
        cur = lo
        while cur_l < mid and cur_r < hi:
            if left[cur_l - lo] < array[cur_r]:
                array[cur] = left[cur_l - lo]
                cur_l += 1
            else:
                array[cur] = array[cur_r]
                cur_r += 1
            cur += 1
        else:
            if cur_r == hi:
                array[cur:hi] = left[cur_l - lo:]

    def merge_sort_c_range(lo, hi):
        if hi - lo >= 2:
            mid = (hi + lo) >> 1
            # print(f'merge_sorting lo:{lo},  mid:{mid}')
            merge_sort_c_range(lo, mid)
            # print(f'merge_sorting mid:{mid}, hi:{hi}')
            merge_sort_c_range(mid, hi)
            merge(lo, mid, hi)
        else:
            # print(f'ending as {array[lo:hi]}')
            return

    return merge_sort_c_range(0, len(array))


